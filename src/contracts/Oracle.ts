import { createUseReadContract } from 'wagmi/codegen'

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Oracle
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export const oracleAbi = [
  {
    type: 'function',
    inputs: [
      { name: '_collateralId', internalType: 'bytes32', type: 'bytes32' }
    ],
    name: 'getPrice',
    outputs: [{ name: '', internalType: 'uint256', type: 'uint256' }],
    stateMutability: 'view'
  }
] as const

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// React
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/**
 * Wraps __{@link useReadContract}__ with `abi` set to __{@link oracleAbi}__
 */
export const useReadOracle = /*#__PURE__*/ createUseReadContract({
  abi: oracleAbi
})

/**
 * Wraps __{@link useReadContract}__ with `abi` set to __{@link oracleAbi}__ and `functionName` set to `"getPrice"`
 */
export const useReadOracleGetPrice = /*#__PURE__*/ createUseReadContract({
  abi: oracleAbi,
  functionName: 'getPrice'
})
