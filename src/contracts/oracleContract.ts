import { createUseReadContract } from 'wagmi/codegen'

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// oracleContract
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export const oracleContractAbi = [
  {
    type: 'function',
    inputs: [
      { name: '_collateralId', internalType: 'bytes32', type: 'bytes32' }
    ],
    name: 'getPrice',
    outputs: [{ name: '', internalType: 'uint256', type: 'uint256' }],
    stateMutability: 'view'
  }
] as const

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// React
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

/**
 * Wraps __{@link useReadContract}__ with `abi` set to __{@link oracleContractAbi}__
 */
export const useReadOracleContract = /*#__PURE__*/ createUseReadContract({
  abi: oracleContractAbi
})

/**
 * Wraps __{@link useReadContract}__ with `abi` set to __{@link oracleContractAbi}__ and `functionName` set to `"getPrice"`
 */
export const useReadOracleContractGetPrice =
  /*#__PURE__*/ createUseReadContract({
    abi: oracleContractAbi,
    functionName: 'getPrice'
  })
